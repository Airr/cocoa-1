#include <stdio.h>
#include <stdlib.h>
#include <sys/poll.h>
#include "alder_file_isstdin.h"

int alder_file_isstdin()
{
    struct pollfd fds;
    int ret;
    fds.fd = 0; /* this is STDIN */
    fds.events = POLLIN;
    ret = poll(&fds, 1, 0);
    return ret;
}
